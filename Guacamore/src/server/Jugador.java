package server;

/**
 *
 * @author PLANZAGOM
 */
public class Jugador {
    private String nombre;
    private int id;
    private int score;

    public Jugador(String nombre, int id, int score) {
        this.nombre = nombre;
        this.id = id;
        this.score = score;
    }
    
    

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
    
    
}
